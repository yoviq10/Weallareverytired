package com.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		int z=10;
		z=z+1;
		
		z+=1;  // short hand way of writing the code
		System.out.println(z);
		
		z++;   // Increment Variable, it can only add 1 to the variable
		System.out.println(z);
		
		int x=20;
		x=1;
		x--; //decrement operator--> subtract 1 from the variable. They use 2 dashes
		System.out.println(x);

		//**you can use your increment and decrement ONYL WITH variables NOT VALUES**
		
		
		
		
		
		
		
	}

}
