public class RelationalOperators {
	
	public static void main(String[] args) {
		
		
	int a=10;
	int b=7;
	System.out.println(a>b); //prints out TRUE
	System.out.println(a<b); //pri nts out FALSE
	System.out.println(a==b);// prints out FALSE
	System.out.println(a!=b); //prints out TRUE >>>!= (not equal)
	
	
	System.out.println("------------------------");
//result of operation operator will always be boolean value
		
		boolean result=a>=b; //print come out as TRUE
		boolean result1=a<=b; // print comes out as FALSE
		System.out.println(result);
		System.out.println(result1);
		
	System.out.println("----------");
	
	int c=20;
	int d=29;
	
	System.out.println(c==d);// checking equality
	System.out.println(c=d); //assigning the value
	System.out.println(c); //prints 29
	System.out.println(d); // prints 29
	
	
			
			
			
			
		
	}
	

}
