package ReviewClass4;

public class NestedForLoopExample2 {

	public static void main(String[] args) {
		

		

		for(int i=0; i<5; i++) {
			
			for(int j=0; i<5; j++) { // I & J increase, both start off at 0 and works up to 4 so that it is less than 5
				
				System.out.println(i+" "+j);
				i++;
			}
			
		}
		
		
		
		
		
		
		
		
		
	}

}
